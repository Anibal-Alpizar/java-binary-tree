    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Verificamos que los campos no estén vacíos
        if (txtId.getText().isEmpty() || txtMarca.getText().isEmpty() || txtAnoCreacion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int id = Integer.parseInt(txtId.getText());
            String marca = txtMarca.getText();
            int anoCreacion = Integer.parseInt(txtAnoCreacion.getText());

            if (id <= 0) {
                JOptionPane.showMessageDialog(this, "El ID debe ser un número entero positivo.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (anoCreacion <= 0 || txtAnoCreacion.getText().length() != 4) {
                JOptionPane.showMessageDialog(this, "El año de creación debe ser un número entero positivo de 4 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String so = cmbSos.getSelectedItem().toString();

            Movil movil = new Movil(id, marca, anoCreacion, so);

            if (arbol.buscarId(arbol.raiz, id)) {
                JOptionPane.showMessageDialog(this, "Error: El ID " + id + " ya existe en el árbol.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            arbol.insertarNodo(movil);

            limpiarCampos();

            JOptionPane.showMessageDialog(this, "Móvil insertado correctamente en el árbol binario.");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese números válidos para el ID y el año de creación.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        try {
            int idAEliminar = Integer.parseInt(txtEliminar.getText());

            if (!arbol.buscarId(arbol.raiz, idAEliminar)) {
                JOptionPane.showMessageDialog(this, "El ID " + idAEliminar + " no existe en el árbol.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (arbol.tieneDosHijos(idAEliminar)) {
                JOptionPane.showMessageDialog(this, "No se puede eliminar el nodo con ID " + idAEliminar + " porque tiene dos hijos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            arbol.eliminarNodo(idAEliminar);

            JOptionPane.showMessageDialog(this, "Nodo con ID " + idAEliminar + " eliminado correctamente del árbol binario.");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un número válido para el ID.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

