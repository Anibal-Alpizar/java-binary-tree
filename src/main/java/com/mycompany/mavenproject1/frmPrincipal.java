        StringBuilder stringBuilder = new StringBuilder();

        for (Nodo nodo : nodosHojas) {
            stringBuilder.append("ID: ").append(nodo.movil.getId()).append(" - Marca: ").append(nodo.movil.getMarca()).append(" - Año de Creación: ").append(nodo.movil.getAnhoCreacion()).append(" - Sistema Operativo: ").append(nodo.movil.getSO()).append("\n");
        }

